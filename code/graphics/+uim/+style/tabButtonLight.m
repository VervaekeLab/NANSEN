    methods (Static)
        function rgb = getFocusColor
            try
                color = javax.swing.UIManager.get('Focus.color');
                rgb = cellfun(@(name) get(color, name), {'Red', 'Green', 'Blue'});
                rgb = rgb/255;
            catch
                rgb = [0.8980, 0.3529, 0.0353];
            end
        end
    end
